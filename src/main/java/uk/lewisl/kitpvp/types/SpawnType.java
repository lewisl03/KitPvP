package uk.lewisl.kitpvp.types;

public class SpawnType {
    String world;
    double x;
    double y;
    double z;


    public SpawnType(String world, double x, double y, double z) {
        this.world = world;
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public String getWorld() {
        return world;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getZ() {
        return z;
    }
}
